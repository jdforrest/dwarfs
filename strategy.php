                <h2>Strategy</h2>
                <div>
                    <ul>
                        <li>
                            <span class="bold">Anti-magic: </span>Runelord (can be given defense runes) or two Runesmiths with Master Rune of Balance. Total swing dice must be 4.
                        </li>
                        <li>
                            <span class="bold">Great Weapon Warriors:</span> ~35-40, no less than 25. Longbeards are really only good if they’re rangers.
                        </li>
                        <li>
                            <span class="bold">Hammerers: </span>always a good idea. Large unit of 30+.
                        </li>
                        <li>
                            <span class="bold">Quarreller Rangers with Great Weapons: </span>~20. No standard, but take musician. During deployment, expose war machines to entice enemy’s fast units, then pop these guys in front of them afterwards.
                        </li>
                        <li>
                            <span class="bold">Thunderers: </span>Usually a grudge thrower or an organ is a better option. Taken small with shields (~10 - 15), they can be good for distraction and redirection.
                        </li>
                        <li>
                            <span class="bold">Miners: </span>Excellent in anvil lists. Best against weak enemy support units like missile troops, lone characters, anvil/ark/cauldron-style thingies, and warmachines. Excellent flank and rear charge support for main blocks. Avoid blasting charges. ~15 - 20: Add musician, banner, champion, steam drill in that order of importance. OR ~15 - 20: musician at most. Used as bait, table quarter grabbers, bully unit.
                        </li>
                        <li>
                            <span class="bold">Tanked Battle Standard Bearer: </span>Give him Master Rune of Gromril, no question.  If possible, Resistance and Spite as well. Most armies make a thane the BSB.
                        </li>
                        <li>
                            <span class="bold">Cannons: </span>Need to kill big things like hydras or abominations.  Take a cannon with runes of Forging and Burning. Only need one cannon with Burning, the other just needs Forging. Add Engineer. Target priority:
                            <ol>
                                <li>
                                    Big Monsters
                                </li>
                                <li>
                                    Artillery Hunters (organ gun is best for this though, but can use grapeshot with cannons)
                                </li>
                                <li>
                                    Elite Infantry & Cavalry: cannon shots are largely a waste though, Grudge Thrower would be better.
                                </li>
                                <li>
                                    Enemy Artillery: necessary if enemy has castled.
                                </li>
                            </ol>
                        </li>
                        <li>
                            <span class="bold">Organ gun: </span>Awesome, but can’t take an engineer and blows up fairly regularly. Good for flank protection, knight killers, skirmisher slayers, solo-targets, artillery park guardians. Multi-role and effective. Often re-roll 2s, let 4s go (4 hits better than none, and only half the time will you get more). Always let 6s and up stand.
                        </li>
                        <li>
                            <span class="bold">Bigger is always better: </span>Always step-up and steadfast and horde. Stop magic and stop artillery, and the negatives of big blocks will be negated. 25 - 40 GW Warrior, three rank blocks are choice.
                        </li>
                        <li>
                            <span class="bold">Characters are secondary to just more great weapons. </span>Building combat characters is usually a bad idea.
                        </li>
                        <li>
                            <span class="bold">Characters should be tough as nails.</span> Weapon runes aren’t a bad idea, but they’re really just backup to good armor. Rune of fire and maybe cleaving is limit for character killing.
                        </li>
                        <li>
                            <span class="bold">Shields are for suckers: </span>Really only used for anti-missile fire if your warriors have great weapons, and a lot of armies ignore shooting. For those that do, Master Rune of Grungni and simply more bodies is the answer. One unit of shield dwarfs as an anvil is a decent option. Good for character escort. However, will need to make them a big unit so they can last long enough for a unit to back them up with real killing.
                        </li>
                        <li>
                            <span class="bold">Aggression Levels:</span>
                            <ol>
                                <li>
                                    <span class="bold">Aggressive: </span>All dwarf surge forward ASAP. Least effective.
                                </li>
                                <li>
                                    <span class="bold">Cautious:</span> Deploy forward and move out after artillery has fired one to two times. Appropriate for most battles, battles where armies have small to no amounts of massive units or giant monsters or tons of skirmishing fast flankers.
                                </li>
                                <li>
                                    <span class="bold">Castle: </span>Defensive deployment central and about halfway into deployment zone. Artillery deployed behind blocks, most blocks of warriors should be in deep formation to start. If artillery isn’t killing enough, prepare to back up two inches a turn. Be prepared to sacrifice artillery to draw enemy into flank charge. Only fight when they have been severly depleted from artillery fire.
                                </li>
                                <li>
                                    <span class="bold">Hard Castle: </span>Find a corner or favorable terrain and circle the wagons. Effective against fast armies, deploy hammerers or GW warriors in long line in front of artillery with warriors in deep formation on sides with no more than one inch between each unit. May limit line of sight for artillery. Kill everything that flies & kill monsters early too. In this situation, playing for a draw, with a possible win if they are too aggressive. Least fun battles.
                                </li>
                            </ol>
                        </li>
                    </ul>

